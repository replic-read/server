package com.rere.server.domain.model.replic;

/**
 * Different media modes for a replic.
 */
public enum MediaMode {

    /**
     * All media, i.e. images an videos, are replicated.
     */
    ALL,

    /**
     * Only images are replicated.
     */
    IMAGES,

    /**
     * No media is replicated.
     */
    NONE

}
